class Машина:
    def __init__(self, марка, модель, год_выпуска):
        self.марка = марка
        self.модель = модель
        self.год_выпуска = год_выпуска

    def __str__(self):
        return f'Марка: {self.марка}, Модель: {self.модель}, Год выпуска: {self.год_выпуска}'


машина = Машина('BMW', 'M5', 2021)
print(машина)